package onlnevideo;

public class SwitchCase {

	public static void main(String[] args) {
		String a = "def";
		
		switch(a)
		{
		case "abc":
			System.out.println("This is abc");
			break;
			
		case "pwm":
			System.out.println("Not a valid task");
			break;
		
		default:
			System.out.println("Please check your input");
		
		}
		

	}

}
